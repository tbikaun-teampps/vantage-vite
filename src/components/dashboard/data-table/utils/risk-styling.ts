export const getRiskStyle = (risk: string): string => {
  switch (risk) {
    case "high":
      return "cursor-help capitalize bg-red-50 text-red-700 border-red-200 hover:bg-red-100 dark:bg-red-950/50 dark:text-red-400 dark:border-red-700 dark:hover:bg-red-950/70 font-medium";
    case "medium":
      return "cursor-help capitalize bg-yellow-50 text-yellow-700 border-yellow-200 hover:bg-yellow-100 dark:bg-yellow-950/50 dark:text-yellow-400 dark:border-yellow-700 dark:hover:bg-yellow-950/70 font-medium";
    default:
      return "cursor-help capitalize bg-green-50 text-green-700 border-green-200 hover:bg-green-100 dark:bg-green-950/50 dark:text-green-400 dark:border-green-700 dark:hover:bg-green-950/70 font-medium";
  }
};